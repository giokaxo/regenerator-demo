
export default class SmartInvoiceCredentials {
  protected _fields: Record<string, any> = {};

  constructor(data: any = {}) {
    if (Object.prototype.hasOwnProperty.call(data, 'merchantId')) {
      this.setMerchantId(data['merchantId']);
    }
    if (Object.prototype.hasOwnProperty.call(data, 'applicationId')) {
      this.setApplicationId(data['applicationId']);
    }
  }


  get merchantId(): string {
    return this._fields['merchantId'];
  }
  
  getMerchantId(): string {
      return this._fields['merchantId'];
  }

  setMerchantId(value: string) {
    this._fields['merchantId'] = value;

    return this;
  }

  get applicationId(): string {
    return this._fields['applicationId'];
  }
  
  getApplicationId(): string {
      return this._fields['applicationId'];
  }

  setApplicationId(value: string) {
    this._fields['applicationId'] = value;

    return this;
  }

  jsonSerialize(): Record<string, any> {
    const data: Record<string, any> = {};
    if (Object.prototype.hasOwnProperty.call(this._fields, 'merchantId')) {
      data['merchantId'] = this['merchantId'];
    }
    if (Object.prototype.hasOwnProperty.call(this._fields, 'applicationId')) {
      data['applicationId'] = this['applicationId'];
    }

    return data;
  }
}
